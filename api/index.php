                    <h1>
                      <font class="lt32" color="darkred">Studentka wunderkind z Prahy získala nejvyšší lékařské ocenění
                        za objev způsobu hubnutí o 15 kg bez chemie, hladu a fyzické zátěže.</font>
                    </h1>
                    <font color="darkred"></font>
                    <p></p>
                    <hr style="border: #cc0000 1px solid;">
                    <div class="e-content">
                      <div class="c-cmr-img text-center">
                        <figure class="js-lightgallery">
                          <img class="img img-responsive" src="index_files/11.jpg" style="width: 450px;">
                          <figcaption>
                            <span class="text-center lt33">Vystoupení Lenky na kongresu</span>
