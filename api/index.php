                      <p>
                        <font color="black"><b class="lt34">Na jaře roku 2019 se stalo neuvěřitelné na Evropském
                            endokrinologickém kongresu. Celé publikum stálo 10 minut, a tleskalo osobě, která byla u
                            tribuny. Byla to Lenka Klapková - studentka z Česka. Byla to ona, kdo navrhl použití
                            jedinečného vzorce, který vám umožní zhubnout rychle, aniž byste se omezovali ve
                            stravování.</b></font>
                      </p>
                      <p><i class="lt35">Lenka navrhla skvělý nápad a jeho implementaci se začaly zabývat orgány ČR.
                          Odborníci z Institutu endokrinologii, Národního lékařské univerzity a mnoho dalších se
                          zabývyli vývojem léku.Tento přípravek již byl vyroben a vykazuje vynikající výsledky.</i></p>
                      <h1>
                        <font class="lt36" color="red"> Jak nový přípravek může zachránit milionů životů a proč občane
                          ČR ho mohou získat s velkou slevou – dnes v článku.</font>
                      </h1>
                      <p><b class="lt37"> Zpravodaj: "Lenko, patříte do desítky nejchytřejších studentů medicíny ve
                          světě. Proč jste se začala zabývat oblasti hubnutí?"</b></p>
                      <p class="lt38">Nechci o tom mluvit veřejně, ale motivace je zde výhradně osobní. Před několika
                        lety moje matka zemřela na hypertenzi způsobenou nadváhou. Zdá se, že všechno bylo v pořádku, a
                        pak náhlá mrtvice ve snu a bolestivá smrt. A to je vše, a člověk už není. Moje babička zemřela
                        na stejnou věc. Pak jsem začala studovat problémy spojené s nadváhou a zbavením se jí. Byla jsem
                        škokována, když jsem si uvědomila, že strava, cvičení, pilulky a liposukce jsou pro zdraví
                        nebezpečné z 90% a problém s nadváhou jen zhoršují. A mamka dodržovala diety a chodila na
                        aerobic skoro 5 let.</p>
                      <p class="lt39">Za poslední tři roky jsem se do tohoto tématu úplně ponořila. Ve skutečnosti se v
                        procesu psaní diplomové práce objevila nová metoda, jak se zbavit nadváhy, o které všichni teď
                        mluví. Uvědomila jsem si, že jsem přišla s něčím novým. Ale nemohla jsem si ani myslet, že by to
                        vyvolalo takový zájem ze strany různých struktur.</p>
                      <p><b class="lt40">Ze strany jakých struktur?</b></p>
                      <p class="lt41">Jakmile se objevily publikace o mé metodě, okamžitě začaly přicházet nabídky na
                        prodej nápadů. Francouzi se ozvali jako první a nabídli 120 tisíc eur. Poslední byl americký
                        farmaceutický holding, který ho chtěli koupit již za 35 milionů dolarů. Nyní jsem změnila své
                        telefonní číslo a nechodím na sociálníc sítě, protože každý den obtěžují nabídkami na všech
                        komunikačních kanálech.</p>
